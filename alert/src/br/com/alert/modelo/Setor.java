package br.com.alert.modelo;

public class Setor {
	
	private Long cod_setor;
	
	private Long cod_sub_setor;
	
	private String nom_setor;
	
	private String nom_setor_reduzido;
	
	private String url_link;
	
	private String url_imagem;
	
	private String ind_ativo;

	





	public String getInd_ativo() {
		return ind_ativo;
	}

	public void setInd_ativo(String ind_ativo) {
		this.ind_ativo = ind_ativo;
	}

	public Long getCod_setor() {
		return cod_setor;
	}

	public void setCod_setor(Long cod_setor) {
		this.cod_setor = cod_setor;
	}

	public Long getCod_sub_setor() {
		return cod_sub_setor;
	}

	public void setCod_sub_setor(Long cod_sub_setor) {
		this.cod_sub_setor = cod_sub_setor;
	}

	public String getNom_setor() {
		return nom_setor;
	}

	public void setNom_setor(String nom_setor) {
		this.nom_setor = nom_setor;
	}

	public String getNom_setor_reduzido() {
		return nom_setor_reduzido;
	}

	public void setNom_setor_reduzido(String nom_setor_reduzido) {
		this.nom_setor_reduzido = nom_setor_reduzido;
	}

	public String getUrl_link() {
		return url_link;
	}

	public void setUrl_link(String url_link) {
		this.url_link = url_link;
	}

	public String getUrl_imagem() {
		return url_imagem;
	}

	public void setUrl_imagem(String url_imagem) {
		this.url_imagem = url_imagem;
	}

	
	
	

}
